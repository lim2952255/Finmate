package com.finmate.domain.stock;

// 주식/증권 상품 종류
public enum StockSecurityType {
    // 보통주다.
    // 회사의 일반적인 지분을 나타내는 주식이며, 보통 의결권이 있다.
    // 예: 삼성전자, NAVER, 현대차.
    COMMON_STOCK,

    // 우선주다.
    // 보통주보다 배당이나 잔여재산 분배에서 우선권이 있지만, 의결권이 없거나 제한되는 경우가 많다.
    // 예: 삼성전자우, 현대차우.
    PREFERRED_STOCK,

    // ETF(Exchange Traded Fund, 상장지수펀드)다.
    // 특정 지수, 산업, 국가, 원자재 등의 수익률을 따라가도록 만든 펀드를 주식처럼 거래하는 상품이다.
    // 예: KODEX 200, TIGER 미국S&P500.
    ETF,

    // ETN(Exchange Traded Note, 상장지수증권)이다.
    // 증권사가 발행한 채권형 상품으로, 특정 지수나 자산 수익률을 추종한다.
    // ETF와 비슷하게 거래되지만 발행 증권사의 신용위험이 있다.
    ETN,

    // ETC(Exchange Traded Commodity)다.
    // 금, 은, 원유 같은 원자재 가격을 추종하는 상장 상품을 의미한다.
    ETC,

    // REIT(Real Estate Investment Trust, 부동산투자회사)다.
    // 투자자 자금을 모아 부동산에 투자하고 임대수익이나 매각수익을 배당하는 상품이다.
    REIT,

    // ELW(Equity Linked Warrant, 주식워런트증권)다.
    // 특정 주식이나 지수를 기초자산으로 하며, 가격 변동에 레버리지로 투자하는 파생상품 성격의 증권이다.
    // 일반 주식보다 위험도가 높다.
    ELW,

    // DR(Depositary Receipt, 주식예탁증서)다.
    // 외국 기업의 주식을 다른 나라 시장에서 거래할 수 있도록 만든 증서다.
    // 예: ADR, GDR.
    DR,

    // 펀드 상품이다.
    // 여러 투자자의 돈을 모아 주식, 채권, 부동산 등 다양한 자산에 투자하는 금융상품이다.
    FUND,

    // 증권투자회사다.
    // 투자 목적으로 설립되어 주식, 채권 등 유가증권에 투자하는 회사 형태의 상품이다.
    INVESTMENT_COMPANY,

    // 선박투자회사다.
    // 선박에 투자하고 운항수익, 임대수익, 매각수익 등을 투자자에게 배분하는 특수 목적 투자회사다.
    SHIP_INVESTMENT_COMPANY,

    // 사회간접자본 투융자회사 또는 인프라펀드 성격의 상품이다.
    // 도로, 항만, 철도 같은 인프라 자산에 투자하고 수익을 배분하는 상품이다.
    INFRASTRUCTURE_FUND,

    // SPAC(Special Purpose Acquisition Company, 기업인수목적회사)이다.
    // 비상장 기업과의 합병을 목적으로 상장된 회사이며, 일반 영업회사와 분석 방식이 다르다.
    SPAC,

    // 지수다.
    // 개별 종목이 아니라 시장이나 특정 종목군의 가격 흐름을 나타내는 기준값이다.
    // 예: KOSPI, KOSDAQ, NASDAQ Composite.
    INDEX,

    // 워런트다.
    // 특정 가격에 주식 등을 사거나 팔 수 있는 권리를 나타내는 상품이다.
    // 신주인수권증권, 신주인수권증서 같은 권리성 증권을 포함한다.
    WARRANT,

    // 그 밖의 상품 유형이다.
    // 코드 체계상 분류는 가능하지만 현재 enum에서 구체적인 금융상품명으로 나누지 않은 경우 사용한다.
    OTHER,

    // 알 수 없는 상품 유형이다.
    // 외부 마스터 파일에서 아직 해석하지 못한 코드가 내려왔을 때 시스템을 깨뜨리지 않고 보관하기 위해 사용한다.
    UNKNOWN
}
